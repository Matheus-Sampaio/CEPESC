                    <h2>EXPERIÊNCIAS DO NATAL</h2>
                    <cite>Pr. Djalma Torres<cite>
                    <p>Hoje vivi uma experiência inusitada, como pastor. Estava hospitalizado na AMO desde às 7h30 da manhã, quando por volta das 11h, alguém da TV Bahia me telefonou pedindo para gravar uma mensagem de Natal, que concordei. Perguntado sobre onde eu me encontrava, respondi que estava hospitalizado. Houve um espanto muito grande, com a pergunta sobre o que havia acontecido. Respondi que estava tudo bem, estava cuidando de um problema de saúde.</p>
                    <p>Marcamos a gravação para depois do tratamento, por volta das 13h. Deixei o espaço da sessão do internamento, tentei mudar o perfil de doente sofrido, saindo de uma sessão de quimioterapia, para um pastor com uma rica e entusiasmada experiência a respeito do relato bíblico da encarnação divina, em celebrações sempre repetidas anualmente, mas também sempre emocionantes, do nascimento de Jesus.</p>
                    <p>E fui para a recepção, local acertado para a entrevista. Depois de instalados os equipamentos e pronta a equipe, me preparei para começar, quando chegou ao local um representante da AMO querendo barrar a entrevista, julgando que se tratava de alguma denúncia, o que me levou a esclarecer que eu era pastor protestante e que se tratava de uma entrevista sobre o Natal. Logo em seguida veio outro representante, que recebeu a mesma explicação de minha parte e a garantia da equipe de TV de que esse era realmente o tema da gravação. Mas, para ter certeza, ele permaneceu no local até o fim da entrevista.</p>
                    <p>Além dessa situação, pacientes de todo jeito entravam e saiam, me deixando meio desconfortável com o inusitado da experiência, tão diferente de uma das muitas entrevista que tenho dado em locais mais adequado, em celebrações com grupos e em Igrejas, devidamente paramentado e embalado por liturgias bem elaboradas nas muitas comemorações do Natal de Jesus Cristo.</p>
                    <p id="SAUDADES PERENES">Mas me consolei com o fato de que Jesus nasceu num lugar inóspito, sem conforto e na pobreza da falta de condições apropriadas para o nascimento de um ser humano, não obstante ter Ele vindo ao mundo para anunciar e viver o projeto divino de reconciliação desse mesmo ser humano com o Deus Eterno.</p>
                    <p class="direita">Salvador, Bahia, 24 de dezembro de 2019</p>
