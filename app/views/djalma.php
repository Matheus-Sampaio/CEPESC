                    <h2>ÍCONES DA MINHA VIDA</h2>
                    <p>Eu havia colocado como ícones da minha vida cultural, no sentido mais amplo e compreensível do termo, incluindo o conhecimento social, teológico, filosófico, psicanalítico e do mundo, três grandes personagens, incontestavelmente exponenciais: Hans Kung, teólogo suíço, autor de duas obras fascinantes: Ser Cristão (em português) e Existe Dios? (em espanhol), entre muitas outras; Pierre Teilhard de Chardin, Jesuíta, Teólogo, Paleontólogo, escritor, antropólogo, cientista e místico, autor de O Fenômeno Humano, O Meio Divino e diversas outras obras; e Sigmund Freud, o pai da Psicanálise e autor de inúmeras obras sobre o inconsciente humano.</p>
                    <p>E estava feliz nessa tríplice companhia, quando o meu amigo Raimundo Dantas posta no Facebook, um texto autobiográfico de Bertrand Russel, que me deixou fascinado, porque representa o que sinto, o que venho fazendo e o que estou buscando incessantemente.</p>
                    <h3>AS TRÊS PAIXÕES</h3>
                    <cite>Bertrand Russel<cite>
                    <p>Três paixões, simples, mas esmagadoramente intensas, têm governado minha vida; a ânsia de amor, a busca do conhecimento e uma insuportável piedade pelo sofrimento da humanidade. Essas três paixões, como grandes vendavais, me têm levado de cá para lá, por uma rota cambiante, sobre um profundo oceano de angústia, até à borda mesmo do desespero.</p>
                    <p>Busquei o amor, primeiro, porque conduz ao êxtase, um êxtase tão grande que, com frequência, teria sacrificado o resto da minha existência por umas horas deste gozo. Busquei-o, em segundo lugar, porque alivia a solidão, essa terrível solidão em que uma consciência trêmula se assoma à beira do mundo para observar o frio e insondável abismo sem vida. Busquei-o, finalmente, porque na união do amor tenho visto, em miniatura mística, a visão antecipada do Céu imaginado por santos e poetas. Isto era o que buscava e, ainda que pudesse parecer demasiado bom para esta vida humana, isto é o que – ao fim –encontrei.</p>
                    <p>Com igual paixão busquei o conhecimento. Desejei entender o coração dos homens. Desejei saber por que brilham as estrelas. E tratei de apreender o poder pitagórico em virtude do qual o número domina o fluxo. Algo disto consegui, embora não muito.</p>
                    <p>O amor e o conhecimento, na medida em que ambos eram possíveis, me transportavam ao céu. Porém sempre a piedade me fazia voltar à terra. Ressoa em meu coração o eco dos gritos de dor. Crianças famintas, vítimas torturadas por opressores, anciãos desvalidos, carga odiosa para seus filhos, e todo um mundo de solidão, pobreza e dor convertem em uma burla o que deveria ser a existência humana. Desejo ardentemente aliviar o mal, porém não posso, e eu também sofro.</p>
                    <p>Esta foi minha vida. Julguei-a digna de se viver, e com gosto voltaria a vivê-la se se me oferecesse a oportunidade.</p>
