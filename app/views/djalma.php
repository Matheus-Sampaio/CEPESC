                    <h2>MOÇÃO DE APOIO AOS EXCLUÍDOS</h2>
                    <cite>Pr Djalma Torres<cite>
                    <p>Como fundador e pastor da Igreja Batista Nazareth, ao longo de mais de trinta anos, sempre apoiei os movimentos ambientais, sociais, culturais, ecumênicos, de diálogo inter-religioso e de luta contra a intolerância religiosa de maneira clara e efetiva.</p>
                    <p>Ao tentar organizar o meu arquivo, deparei com esta moção que disponibilizo na minha página pessoal no site do CEPESC:</p>
                    <p>A Igreja Batista Nazareth, de Salvador, Bahia, que sempre esteve identificada com a luta pela ética e pela justiça, junta-se em solidariedade e apoio ao Grito dos Excluídos deste ano.</p>
                    <p>Reafirmamos a nossa fé num Deus Eterno, justo e sensível ao clamor do povo, que dá forças aos que se mobilizam para a conquista humana da terra, o direito à saúde, educação e moradia.</p>
                    <p>Como igreja cristã, Nazareth continuará a apoiar a luta dos excluídos. Entendemos que a existência dos milhões de excluídos, hoje, no Brasil, revela uma profunda e triste situação anticristã.</p>
                    <p>Acreditamos que o clamor do povo será determinante para o despertamento do poder público para os problemas sociais, como a justa distribuição da terra, da habitação digna, de mais educação e assistência à saúde. É assim que se cumprirá a Justiça Divina.</p>
                    <p>Desse modo, fazemos a nossa confissão pública, neste sete de setembro.</p>
                    <p class="direita">Salvador, Bahia, 7 de setembro de 1995</p>
